const fmtTime = (v: any): string => str(v).replace(/\s+([AaPp][Mm])$/, '$1');

export function buildTransshipmentRoutingBeanList(r: any, moduleType: any) {
  const ports = r?.routingFormData?.transshipmentPorts || [];
  if (ports.length === 0) {
    return [
      {
        referenceNumber: '',
        moduleCode: moduleType,
        transshipmentPortSeq: 0,
        transshipmentPortCode: null,
        transshipmentPortName: null,
      },
    ];
  }

  return ports.map((p: any, idx: number) => ({
    referenceNumber: '',
    moduleCode: moduleType,
    transshipmentPortSeq: p.id,
    transshipmentPortCode: str(p.portCode),
    transshipmentPortName: str(p.portName),
  }));
}

