const mapTransshipmentPorts = (list: any[] | null) =>
 (list ?? []).map((r: any, i: number) => ({
    id: r.transshipmentPortSeq || i,
    portCode: r.transshipmentPortCode ?? '',
    portName: r.transshipmentPortName ?? '',
    eta: parseApiDate(r.transshipmentPortDate ?? null),
  }));

const mapManufacturerNames = (bean: any) =>
  (bean ?? []).map((m: any) => ({ name: m.manufacturerName ?? '', id: m.manufacturerDetailId ?? '' }));

const normalizeCarrierCode = (val: any) => {
  const v = str(val);
  if (v.includes(' - ')) {
    return v.split(' - ')[0].substring(0, 10);
  }
  return v.substring(0, 10);
};